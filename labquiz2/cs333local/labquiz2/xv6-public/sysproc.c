int
sys_shm_get(void)
{
  int addr;
  int key;

  // if (argint(0,&addr)  < 0) return 0;
  if (argint(0,&key) < 0) return 0;
  //  Obtain arguments
  //  determine user virtual page address for mapping shared page
  //    if current address is not page-aligned, skip to a page-aligned
  //    address
  addr = myproc()->sz;
  addr = PGROUNDUP(addr);
  if(shm_growproc(key) < 0)
    return -1;

  return addr;
}



